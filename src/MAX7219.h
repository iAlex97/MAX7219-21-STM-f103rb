#ifndef MAX7219_H_
#define MAX7219_H_


#define REG_NO_OP				0b0000000000000000

#define REG_DIGIT_0				0b0000000100000000
#define REG_DIGIT_1				0b0000001000000000
#define REG_DIGIT_2				0b0000001100000000
#define REG_DIGIT_3				0b0000010000000000
#define REG_DIGIT_4				0b0000010100000000
#define REG_DIGIT_5				0b0000011000000000
#define REG_DIGIT_6				0b0000011100000000
#define REG_DIGIT_7				0b0000100000000000

#define REG_DECODE_MODE			0b0000100100000000
#define REG_INTENSITY			0b0000101000000000
#define REG_SCAN_LIMIT			0b0000101100000000
#define REG_SHUTDOWN			0b0000110000000000

#define REG_TEST_DISP			0b0000111100000000

/**
 * Constants that should be set to `REG_SHUTDOWN`
 */
#define CONST_SHUTDOWN_MODE		0b0000000000000000
#define CONST_NORMAL_OP			0b0000000000000001

/**
 * Constants that should be set to `DECODE_MODE`
 */
#define CONST_DECODE_MODE_1		0b0000000000000000	// No decode

#define CONST_DECODE_MODE_2		0b0000000000000001	// Code B decode for digit 0
														// No decode for digits 7–1

#define CONST_DECODE_MODE_3		0b0000000000001111	// Code B decode for digits 3–0
														// No decode for digits 7–4

#define CONST_DECODE_MODE_4		0b0000000011111111	// Code B decode for digits 7–0

/**
 * Constants that should be set to `REG_SCAN_LIMIT`
 */
#define CONST_SCAN_LIMIT_0		0b0000000000000000	// Display digit 0 only*
#define CONST_SCAN_LIMIT_1		0b0000000000000001	// Display digits 0 & 1*
#define CONST_SCAN_LIMIT_2		0b0000000000000010	// Display digits 0 1 2*
#define CONST_SCAN_LIMIT_3		0b0000000000000011	// Display digits 0 1 2 3
#define CONST_SCAN_LIMIT_4		0b0000000000000100	// Display digits 0 1 2 3 4
#define CONST_SCAN_LIMIT_5		0b0000000000000101	// Display digits 0 1 2 3 4 5
#define CONST_SCAN_LIMIT_6		0b0000000000000110	// Display digits 0 1 2 3 4 5 6
#define CONST_SCAN_LIMIT_7		0b0000000000000111	// Display digits 0 1 2 3 4 5 6 7

/**
 * Constants that should be set to `REG_INTENSITY`
 */
#define CONST_INTENSITY_0		0b0000000000000000
#define CONST_INTENSITY_1		0b0000000000000001
#define CONST_INTENSITY_2		0b0000000000000010
#define CONST_INTENSITY_3		0b0000000000000011
#define CONST_INTENSITY_4		0b0000000000000100
#define CONST_INTENSITY_5		0b0000000000000101
#define CONST_INTENSITY_6		0b0000000000000110
#define CONST_INTENSITY_7		0b0000000000000111
#define CONST_INTENSITY_8		0b0000000000001000
#define CONST_INTENSITY_9		0b0000000000001001
#define CONST_INTENSITY_A		0b0000000000001010
#define CONST_INTENSITY_B		0b0000000000001011
#define CONST_INTENSITY_C		0b0000000000001100
#define CONST_INTENSITY_D		0b0000000000001101
#define CONST_INTENSITY_E		0b0000000000001110
#define CONST_INTENSITY_F		0b0000000000001111

/**
 * Various digits and letters for no decoding mode
 */
#define CONST_ZERO				0b0000000001111110
#define CONST_ONE				0b0000000000110000
#define CONST_TWO				0b0000000001101101
#define CONST_THREE				0b0000000001111001
#define CONST_FOUR				0b0000000000110011
#define CONST_FIVE				0b0000000001011011
#define CONST_SIX				0b0000000001011111
#define CONST_SEVEN				0b0000000001110000
#define CONST_EIGHT				0b0000000001111111
#define CONST_NINE				0b0000000001111011
#define CONST_NONE				0b0000000000000000


/**
 * Defines to control individual segments
 */
#define CONST_SEG_DP			0b0000000010000000
#define CONST_SEG_A				0b0000000001000000
#define CONST_SEG_B				0b0000000000100000
#define CONST_SEG_C				0b0000000000010000
#define CONST_SEG_D				0b0000000000001000
#define CONST_SEG_E				0b0000000000000100
#define CONST_SEG_F				0b0000000000000010
#define CONST_SEG_G				0b0000000000000001

#endif /* MAX7219_H_ */
